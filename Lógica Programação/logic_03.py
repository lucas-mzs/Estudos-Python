# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# Positivo ou negativo (int sem sinal é considerado positivo)

# float -> Número com ponto flutuante
# O tipo float representa qualquer número positvo ou negativo com ponto flutuante
# float sem sinal é considerado positivo

# A função type mostra o tipo que o Python inferiu o valor.

print(11) # int
print(-11) # int

print(1.1) # float
print(155.1644684) # float

print( type('Lucas') )