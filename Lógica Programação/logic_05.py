# Variáveis são usados para salvar algo na memória do computador.
# PEP8: inicie variáveis com letras minúsculas, pode usar número e underline.
# O sinal de ( = ) é o operador de atribuição. Ele é usado para atribuir um valor a um nome (variável).
# Uso: nome_variavel = expressão


nome_completo = 'Lucas Felipe De Menezes Ferreira'
soma_dois_mais_dois = 2 + 2
print(nome_completo, soma_dois_mais_dois, sep=(' - '))

